let firstFavNumb = 19;
let seconFavNumb = 5;

//GREATER THAN
console.log(firstFavNumb > seconFavNumb)

//LESSTHAN
console.log(firstFavNumb < seconFavNumb)

//GREATER THAN EQUAL
console.log(firstFavNumb >= seconFavNumb)

//LESS THAN EQUAL
console.log(firstFavNumb <= seconFavNumb)

//STRICT EQUALITY
console.log(firstFavNumb === seconFavNumb)

//LOOSE EQUALITY
console.log(firstFavNumb == seconFavNumb)

//STRICT INEQUALITY
console.log(firstFavNumb !== seconFavNumb)

//LOOSE INEQUALITY
console.log(firstFavNumb != seconFavNumb)
